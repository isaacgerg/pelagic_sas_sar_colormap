"""
Perceptually linear blue-to-yellow colormap for SAS/SAR intensity imagery.
  R² (lightness linearity in CAM02-UCS) = 1.0000
  Colorblind-safe (blue-yellow opponent axis)
  Monotonically increasing luminance
"""


def register(force=True):
    """Register 'pelagic' and 'pelagic_r' with matplotlib.

    After calling this you can use cmap="pelagic" in any matplotlib plot.
    """
    matplotlib.colormaps.register(pelagic, force=force)
    matplotlib.colormaps.register(pelagic.reversed(), force=force)
